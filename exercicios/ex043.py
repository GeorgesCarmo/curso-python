# Crie um programa que mostre na tela todos os números pares que estão no intervalo entre 1 e 50.

for n in range(2, 51, 2):
    print(n, end=' ')
print("FIM")
# Crie um programa que mostre na tela todos os números ímpares que estão no intervalo entre 1 e 50.
for n in range(1, 51, 2):
    print(n, end=' ')
print("FIM")
# Crie um programa que mostre na tela todos os números múltiplos de 3 que estão no intervalo entre 1 e 50.
